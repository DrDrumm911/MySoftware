class Button:
    def click(self):
        print("Click")

# You can create an instance of the class and call the click() method
button_instance = Button()
button_instance.click()  # This will print "Click"

class Screen:
    def welcome(self):
        print("New Screen")

# Create an instance of the class
screen_instance = Screen()

# Call the welcome() method
screen_instance.welcome()  # This will print "New Screen"



